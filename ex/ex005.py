### Crie um programa que leia um numero inteiro e mostre na tela o seu sucessor e seu antecessor.
n1 = int(input('Digite um numero: '))
print('Seu numero {}, Antecessor {}, Sucessor {}.'.format(n1, (n1-1), (n1+1)))

